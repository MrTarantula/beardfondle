function beard() {
    var fart = '';
    for (var i = 0, len = this.length; i < len; i++) {
        if (symbols[this[i]]) {
            fart += symbols[this[i]];
            if (i !== len - 1) {
                fart += '+';
            }
        } else {
            throw 'Bad character somewhere';
        }
    }
    return fart;
}

function fondle() {
    var acceptable = '!+({[:]})';
    for (var i = 0, len = this.length; i < len; i++) {
        if (acceptable.indexOf(this[i]) < 0) {
            throw 'Bad character somewhere';
        }
    }
    /* jshint -W061 */
    return eval('(' + this + ')');
    /* jshint +W061 */
}

